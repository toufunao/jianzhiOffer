package jianzhi;

import java.util.ArrayList;
import java.util.List;
import java.util.Stack;

public class MinStack_30 {
    Stack<Integer> A, B;
    public MinStack_30() {
        A = new Stack<>();
        B = new Stack<>();
    }
    public void push(int x) {
        A.add(x);
        if(B.empty() || B.peek() >= x)
            B.add(x);
    }
    public void pop() {
        if(A.pop().equals(B.peek()))
            B.pop();
    }
    public int top() {
        return A.peek();
    }
    public int min() {
        return B.peek();
    }
}
